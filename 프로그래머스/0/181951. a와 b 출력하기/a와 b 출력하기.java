import java.util.Scanner;

public class Solution {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();

        String A = "a" + " = " + a + "\n";
        String B = "b" + " = " + b;
        System.out.println(A + B);
    }
}